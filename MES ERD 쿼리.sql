  ON PH.LOT_ID = LM.LOT_ID
ORDER BY EM.EQUIPMENT_CODE, PH.START_TIME;

-- 출하현황
SELECT
    SM.SHIPMENT_NO,
    SM.SHIPMENT_DATE,
    CM.CUSTOMER_CODE,
    CM.CUSTOMER_NAME,
    LM.LOT_NO,
    IM.ITEM_CODE,
    IM.ITEM_NAME,
    SD.SHIP_QTY,
    SM.SHIPMENT_STATUS
FROM SHIPMENT_MASTER SM
JOIN CUSTOMER_MASTER CM
  ON SM.CUSTOMER_ID = CM.CUSTOMER_ID
JOIN SHIPMENT_DETAIL SD
  ON SM.SHIPMENT_ID = SD.SHIPMENT_ID
JOIN LOT_MASTER LM
  ON SD.LOT_ID = LM.LOT_ID
JOIN ITEM_MASTER IM
  ON LM.ITEM_ID = IM.ITEM_ID
ORDER BY SM.SHIPMENT_DATE, SM.SHIPMENT_NO;

-- 거래처별 출하현황 집계
SELECT
    CM.CUSTOMER_CODE,
    CM.CUSTOMER_NAME,
    COUNT(SM.SHIPMENT_ID) AS SHIPMENT_COUNT,
    SUM(SD.SHIP_QTY) AS TOTAL_SHIP_QTY
FROM CUSTOMER_MASTER CM
JOIN SHIPMENT_MASTER SM
  ON CM.CUSTOMER_ID = SM.CUSTOMER_ID
JOIN SHIPMENT_DETAIL SD
  ON SM.SHIPMENT_ID = SD.SHIPMENT_ID
GROUP BY CM.CUSTOMER_CODE, CM.CUSTOMER_NAME
ORDER BY CM.CUSTOMER_NAME;

-- 제품불량 현황
SELECT
    LM.LOT_NO,
    PH.PROCESS_CODE,
    PH.PROCESS_NAME,
    DL.DEFECT_CODE,
    DL.DEFECT_NAME,
    DL.DEFECT_QTY,
    DL.DEFECT_REASON,
    DL.CREATED_AT
FROM DEFECT_LOG DL
JOIN LOT_MASTER LM
  ON DL.LOT_ID = LM.LOT_ID
LEFT JOIN PROCESS_HISTORY PH
  ON DL.PROCESS_HIST_ID = PH.PROCESS_HIST_ID
ORDER BY LM.LOT_NO, DL.DEFECT_ID;

-- 품목별 불량 집계
SELECT
    IM.ITEM_CODE,
    IM.ITEM_NAME,
    DL.DEFECT_CODE,
    DL.DEFECT_NAME,
    SUM(DL.DEFECT_QTY) AS TOTAL_DEFECT_QTY
FROM DEFECT_LOG DL
JOIN LOT_MASTER LM
  ON DL.LOT_ID = LM.LOT_ID
JOIN ITEM_MASTER IM
  ON LM.ITEM_ID = IM.ITEM_ID
GROUP BY IM.ITEM_CODE, IM.ITEM_NAME, DL.DEFECT_CODE, DL.DEFECT_NAME
ORDER BY IM.ITEM_CODE, DL.DEFECT_CODE;

-- 공정별 불량 집계
SELECT
    PH.PROCESS_CODE,
    PH.PROCESS_NAME,
    DL.DEFECT_CODE,
    DL.DEFECT_NAME,
    SUM(DL.DEFECT_QTY) AS TOTAL_DEFECT_QTY
FROM DEFECT_LOG DL
JOIN PROCESS_HISTORY PH
  ON DL.PROCESS_HIST_ID = PH.PROCESS_HIST_ID
GROUP BY PH.PROCESS_CODE, PH.PROCESS_NAME, DL.DEFECT_CODE, DL.DEFECT_NAME
ORDER BY PH.PROCESS_CODE, DL.DEFECT_CODE;

-- 자재사용 이력
SELECT
    LM.LOT_NO,
    PH.PROCESS_CODE,
    PH.PROCESS_NAME,
    MU.MATERIAL_CODE,
    MU.MATERIAL_NAME,
    MU.MATERIAL_LOT_NO,
    MU.USED_QTY,
    MU.UNIT,
    MU.CREATED_AT
FROM MATERIAL_USAGE MU
JOIN LOT_MASTER LM
  ON MU.LOT_ID = LM.LOT_ID
LEFT JOIN PROCESS_HISTORY PH
  ON MU.PROCESS_HIST_ID = PH.PROCESS_HIST_ID
ORDER BY LM.LOT_NO, MU.MATERIAL_USAGE_ID;

-- 자재별 사용 집계
SELECT
    MU.MATERIAL_CODE,
    MU.MATERIAL_NAME,
    SUM(MU.USED_QTY) AS TOTAL_USED_QTY,
    MU.UNIT
FROM MATERIAL_USAGE MU
GROUP BY MU.MATERIAL_CODE, MU.MATERIAL_NAME, MU.UNIT
ORDER BY MU.MATERIAL_CODE;

